DROP DATABASE IF EXISTS INVENTORY;
CREATE DATABASE INVENTORY;


USE INVENTORY;



DROP TABLE IF EXISTS BRANDS;
CREATE TABLE BRANDS(
  ID_BRAND INT(11) AUTO_INCREMENT,
  NAME VARCHAR(100) UNIQUE NOT NULL,
  PRIMARY KEY (ID_BRAND)
);


DROP TABLE IF EXISTS PRODUCTS;
CREATE TABLE PRODUCTS(
  ID_PRODUCT INT(11) AUTO_INCREMENT,
  NAME VARCHAR(100) NOT NULL,
  ID_BRAND INT(11),
  DESCRIPTION VARCHAR(500),
  MEASUREMENT_TYPE ENUM('ml','lt','gr','kg','piece') NOT NULL,
  QTTY_MEASUREMENT DOUBLE NOT NULL,
  QTTY_STOCK INT(11) NOT NULL DEFAULT 1,
  PRICE DOUBLE NOT NULL,
  REORDER INT(11) NOT NULL,
  PRIMARY KEY (ID_PRODUCT),
  FOREIGN KEY (ID_BRAND) REFERENCES BRANDS(ID_BRAND)
);


DROP TABLE IF EXISTS SALES;
CREATE TABLE SALES(
  ID_SALE INT(11) AUTO_INCREMENT,
  USER VARCHAR(255),
  `DATE` TIMESTAMP,
  PRIMARY KEY (ID_SALE)
);

DROP TABLE IF EXISTS SALES_DESCRIPTION;
CREATE TABLE SALES_DESCRIPTION(
  ID_SALE INT(11),
  ID_PRODUCT INT(11),
  QUANTITY DOUBLE NOT NULL,
  PRICE DOUBLE NOT NULL,
  PRIMARY KEY (ID_SALE,ID_PRODUCT),
  FOREIGN KEY (ID_SALE) REFERENCES SALES(ID_SALE),
  FOREIGN KEY (ID_PRODUCT) REFERENCES PRODUCTS(ID_PRODUCT)
);

